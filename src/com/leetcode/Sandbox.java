    public int threeSumMulti(int[] A, int target) {
        int[] c = new int[101];
        int m = (int) Math.pow(10, 9) + 7;
        for (int a : A) {
            c[a] += 1;
        }

        int ret = 0;

        if (target % 3 == 0) {
            int a1 = target / 3;
            if (c[a1] >= 3) {
                ret += (long) c[a1] * (c[a1] - 1) * (c[a1] - 2) / 6 % m;
            }
        }

        for (int a1 = Math.max(0, (int) (target - 99) / 2); a1 <= 100; a1++) {
            if (c[a1] < 2) {
                continue;
            }
            int a2 = target - (a1 << 1);
            if (a2 < 0) {
                break;
            }
            if (a2 != a1 && c[a2] > 0) {
                ret += (long) c[a1] * (c[a1] - 1) / 2 * c[a2] % m;
            }
        }

        for (int a1 = 0; a1 <= 100; a1++) {
            if (c[a1] == 0) {
                continue;
            }
            int sum2 = target - a1;
            if (sum2 < 0) {
                break;
            }
            for (int a2 = Math.max(a1 + 1, sum2 - 100); a2 <= 100; a2++) {
                if (c[a2] == 0) {
                    continue;
                }
                int a3 = sum2 - a2;
                if (a3 <= a2) {
                    break;
                }
                if (c[a3] > 0) {
                    ret += (long) c[a1] * c[a2] * c[a3] % m;
                }
            }
        }

        return ret;
    }

